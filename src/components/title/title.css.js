import styled from 'styled-components';
import MEDIA from 'helpers/mediaTemplates';
import { rhythm, scale } from '../../utils/typography';

export const Text = styled.span`
  display: block;
  margin-bottom: ${rhythm(1)};
  font-weight: ${({ size }) => () => {
    switch (size) {
      case 'large':
        return '400';
      default:
        return '500';
    }
  }};
  font-size: ${({ size }) => () => {
    switch (size) {
      case 'large':
        return scale(1);
      default:
        return '2rem';
    }
  }};
  line-height: 1.2;

  ${MEDIA.TABLET`
    font-size: ${({ size }) => () => {
      switch (size) {
        case 'large':
          return '2.6rem';
        default:
          return '2rem';
      }
    }};
  `};
`;
